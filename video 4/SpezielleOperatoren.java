public class SpezielleOperatoren
{
	public static void main(String[] args){
		
		//Variablendeklaration
		int i = 0;
		
		//Spezielle arithmetsiche Operatoren
		i += 3;		// i = 3
		i -= 1;		// i = 2
		i *= 3;		// i = 6
		i /= 2;		// i = 3
		i %= 2;		// i = 1
		
		//Ausgabe
		System.out.println(i);
	
	}
}